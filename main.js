          // –ò—Å–ø–æ–ª—å–∑—É–µ–º –ø—Ä–æ—Å—Ç–æ–π –ø–æ–¥—Ö–æ–¥ —Å–Ω–∞—á–∞–ª–∞
          const trackElement = document.querySelector('.track__name');
